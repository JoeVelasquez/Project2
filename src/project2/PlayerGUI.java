    private void confirmButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_confirmButtonMouseClicked
        // TODO add your handling code here:
        
        String firstName = CharFirstName.getText();
        String lastName = CharLastName.getText();
        
        PlayerClass p1 = new PlayerClass(firstName, lastName);
        
        if(dragonBorn == true)
        {
            p1.selectPlayerRace(1);
        }
        else if(dwarf == true)
        {
            p1.selectPlayerRace(2);
        }
        else if(elf == true)
        {
            p1.selectPlayerRace(3);
        }
        else if(gnome == true)
        {
            p1.selectPlayerRace(4);
        }
        else if(halfElf == true)
        {
            p1.selectPlayerRace(5);
        }
        else if(halfing == true)
        {
            p1.selectPlayerRace(6);
        }
        else if(halfOrc == true)
        {
            p1.selectPlayerRace(7);
        }
        else if(human == true)
        {
            p1.selectPlayerRace(8);
        }
        else
        {
            p1.selectPlayerRace(9);
        }
        
        
        if(barbarian == true)
        {
            p1.selectClassType(1);
        }
        else if(bard == true)
        {
            p1.selectClassType(2);
        }
        else if(cleric == true)
        {
            p1.selectClassType(3);
        }
        else if(druid == true)
        {
            p1.selectClassType(4);
        }
        else if(fighter == true)
        {
            p1.selectClassType(5);
        }
        else if(monk == true)
        {
            p1.selectClassType(6);
        }
        else if(paladin == true)
        {
            p1.selectClassType(7);
        }
        else if(ranger == true)
        {
            p1.selectClassType(8);
        }
        else if(rogue == true)
        {
            p1.selectClassType(9);
        }
        else if(sorceror == true)
        {
            p1.selectClassType(10);
        }
        else if(warlock == true)
        {
            p1.selectClassType(11);
        }
        else
        {
            p1.selectClassType(12);
        }
        
        String strString = strengthStat.getText();
        String dexString = dexterityStat.getText();
        String conString = constitutionStat.getText();
        String intString = intelligenceStat.getText();
        String wisString = wisdomStat.getText();
        String chaString = charismaStat.getText();
        
        int strInt = Integer.parseInt(strString);
        int dexInt = Integer.parseInt(dexString);
        int conInt = Integer.parseInt(conString);
        int intInt = Integer.parseInt(intString);
        int wisInt = Integer.parseInt(wisString);
        int chaInt = Integer.parseInt(chaString);
        
        p1.setStat(0, strInt);
        p1.setStat(1, dexInt);
        p1.setStat(2, conInt);
        p1.setStat(3, intInt);
        p1.setStat(4, wisInt);
        p1.setStat(5, chaInt);
        
    }//GEN-LAST:event_confirmButtonMouseClicked

    private void moreStrMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_moreStrMouseClicked
        // TODO add your handling code here:
        if(totalStats > 0)
        {
            String myString = strengthStat.getText();
            int toInt = Integer.parseInt(myString);
            toInt += 1;       
            strengthStat.setText(""+toInt+"");
            totalStats -= 1;
        }       
        
        totalBaseStat.setText(""+totalStats+"");
    }//GEN-LAST:event_moreStrMouseClicked

